    "            <FILE_NAME> %s </FILE_NAME>" LF
    "            <LINE_NUMBER> %u </LINE_NUMBER>" LF
    "            <CONDITION> %s </CONDITION>" LF
    "          </CUNIT_RUN_TEST_FAILURE>" LF;

