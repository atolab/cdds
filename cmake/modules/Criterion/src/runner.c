static const char
run_ftr[] =
    "  <CUNIT_FOOTER> File Generated By Criterion - %s </CUNIT_FOOTER>" LF
    "</CUNIT_TEST_RUN_REPORT>";

static const char
run_result_hdr[] =
    "  <CUNIT_RESULT_LISTING>" LF;

static const char
run_result_ftr[] =
    "  </CUNIT_RESULT_LISTING>" LF;

static const char
run_suite_hdr[] =
    "    <CUNIT_RUN_SUITE>" LF
    "      <CUNIT_RUN_SUITE_SUCCESS>" LF
    "        <SUITE_NAME> %s </SUITE_NAME>" LF;

static const char
run_suite_ftr[] =
    "      </CUNIT_RUN_SUITE_SUCCESS>" LF
    "    </CUNIT_RUN_SUITE>" LF;

