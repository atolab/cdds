    "<?xml version=\"1.0\" ?>" LF
    "<?xml-stylesheet type=\"text/xsl\" href=\"CUnit-Run.xsl\" ?>" LF
    "<!DOCTYPE CUNIT_TEST_RUN_REPORT SYSTEM \"CUnit-Run.dtd\">" LF
    "<CUNIT_TEST_RUN_REPORT>" LF
    "  <CUNIT_HEADER/>" LF;

